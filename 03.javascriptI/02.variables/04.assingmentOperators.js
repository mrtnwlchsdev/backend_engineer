// Los operadores aritmeticos pueden ser utilizados para reasignar el valor de una variable

let count = 0

count += 10

// El operador + se encarga de sumar 10 a la variable count e inmeditamente asignarle el valor obtenido
// Esta operacion puede ser realizada con el resto de operadores aritmeticos (- / * %)

console.log(count) // 1

// Los operadores de incremento y decremento pueden ser utilizados para aumentar el valor de 1 a una variable

count++
console.log(count) // 11

count--
console.log(count) // 10
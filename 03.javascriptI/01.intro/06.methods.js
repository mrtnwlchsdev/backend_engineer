// Los metodos realizan una tarea especifica en el programa
// Los metodos se definen a traves del nombre del mismo seguido de parentesis ()

// Convertir todos los caracteres de un string a mayuscula
console.log("Hello".toUpperCase()) // HELLO

// Determinar si un string inicia con un caracter especifico
console.log("Hello".startsWith("H")) // true

// Remover los espacios en ambos lados del string
console.log("  Hello World     ".trim()) // Hello World